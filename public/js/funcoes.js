$(function () { /*cria miniatura da imagem*/
    $('#upload').change(function () {
        const file = $(this)[0].files[0]
        const fielder = new FileReader()
        fielder.onloadend = function () {
            $('#prev').attr('src', fielder.result)
        }
        fielder.readAsDataURL(file)
    })
});





